class Player():
	def __init__(self, x, y):
		img = pygame.image.load('img/guy1.png')
		self.image = pygame.transform.scale(img, (40, 80))
		self.rect = self.image.get_rect()
		self.rect.x = x
		self.rect.y = y
		self.vel_y = 0
		
	def update(self):
		#get key presses
		key = pygame.key.get_pressed()
		if key[pygame.K_LEFT]:
			self.rect.x -= 5
		if key[pygame.K_RIGHT]:
			self.rect.x += 5

		if key[pygame.K_UP] == True:
			self.vel_y = -10
			self.jumped = True
		

		self.vel_y = self.vel_y + 1 

		if self.vel_y > 10:
			self.vel_y = 10

		if (self.rect.y > screen_height - 100) and (self.vel_y > 0): # Wenn der Spieler auf dem Boden ist
			self.vel_y = 0


		if (self.rect.y <  100) and (self.vel_y < 0): # Wenn der Spieler ganz oben ist
			self.vel_y = 0


		self.rect.y = self.rect.y + self.vel_y

		screen.blit(self.image, self.rect)

def draw_grid():
	for line in range(0, 20):
		pygame.draw.line(screen, (255, 255, 255), (0, line * tile_size), (screen_width, line * tile_size))
		pygame.draw.line(screen, (255, 255, 255), (line * tile_size, 0), (line * tile_size, screen_height))
		



class World():
	def __init__(self, data):
		self.tile_list = []

		#load images
		dirt_img = pygame.image.load('img/dirt.png')
		grass_img = pygame.image.load('img/grass.png')

		row_count = 0
		for row in data:
			col_count = 0
			for tile in row:
				if tile == 1:
					img = pygame.transform.scale(dirt_img, (tile_size, tile_size))
					img_rect = img.get_rect()
					img_rect.x = col_count * tile_size
					img_rect.y = row_count * tile_size
					tile = (img, img_rect)
					self.tile_list.append(tile)
				if tile == 2:
					img = pygame.transform.scale(grass_img, (tile_size, tile_size))
					img_rect = img.get_rect()
					img_rect.x = col_count * tile_size
					img_rect.y = row_count * tile_size
					tile = (img, img_rect)
					self.tile_list.append(tile)

				col_count += 1
			row_count += 1

	def draw(self):
		for tile in self.tile_list:
			screen.blit(tile[0], tile[1])



